    private void jTPessoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTPessoaMouseClicked
        if (jTPessoa.getSelectedRow() != -1) {
            
            txtNome.setText(jTPessoa.getValueAt(jTPessoa.getSelectedRow(), 1).toString());
            txtEmail.setText(jTPessoa.getValueAt(jTPessoa.getSelectedRow(), 2).toString());
        }

    }//GEN-LAST:event_jTPessoaMouseClicked

    private void jTPessoaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTPessoaKeyReleased
        if (jTPessoa.getSelectedRow() != -1) {
            
            txtNome.setText(jTPessoa.getValueAt(jTPessoa.getSelectedRow(), 1).toString());
            txtEmail.setText(jTPessoa.getValueAt(jTPessoa.getSelectedRow(), 2).toString());
        }
    }//GEN-LAST:event_jTPessoaKeyReleased

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        if (jTPessoa.getSelectedRow() != -1) {
            
            PessoaDAO dao = new PessoaDAO();
            Pessoa pessoa = new Pessoa();
            pessoa.setNome(txtNome.getText());
            pessoa.setEmail(txtEmail.getText());
            pessoa.setId((int) jTPessoa.getValueAt(jTPessoa.getSelectedRow(), 0));
            dao.update(pessoa);
            txtNome.setText("");
            txtEmail.setText("");
            readjTable();
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (jTPessoa.getSelectedRow() != -1) {
            
            PessoaDAO dao = new PessoaDAO();
            Pessoa pessoa = new Pessoa();
            pessoa.setId((int) jTPessoa.getValueAt(jTPessoa.getSelectedRow(), 0));
            dao.delete(pessoa);
            txtNome.setText("");
            txtEmail.setText("");
            readjTable();
        } else JOptionPane.showMessageDialog(jPanel1, "Selecione um produto para excluir");
    }//GEN-LAST:event_btnExcluirActionPerformed

