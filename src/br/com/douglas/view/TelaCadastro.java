    public void readjTable() {
        DefaultTableModel modelo = (DefaultTableModel)jTPessoa.getModel();
        modelo.setNumRows(0);
        PessoaDAO pessoa = new PessoaDAO();
        
        pessoa.read().forEach(p -> {
            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getEmail()
            });
        });
    }
    
